CREATE TABLE restaurant (
id_restaurant SERIAL PRIMARY KEY,
nom    VARCHAR(20),
ville  VARCHAR(20),
pays   VARCHAR(20),
nb_etoiles INTEGER,
nb_tables INTEGER NOT NULL,
nb_couverts INTEGER NOT NULL
                  );

CREATE TABLE plat (
id_plat SERIAL PRIMARY KEY,
nom    VARCHAR(20),
prix REAL,
categorie VARCHAR(20)
                  );

CREATE TABLE ingredient (
id_ingredient SERIAL PRIMARY KEY,
nom    VARCHAR(20),
pays VARCHAR(20)
                  );

CREATE TABLE menu (
id_menu SERIAL PRIMARY KEY,
nom    VARCHAR(20),
prix REAL NOT NULL
                  );

CREATE TABLE recette (
                       id_plat   INTEGER NOT NULL,
                       id_ingredient INTEGER NOT NULL,
                       poids  REAL NOT NULL,
                       --PRIMARY KEY (id_plat, id_ingredient),
                       FOREIGN KEY (id_plat)   REFERENCES plat,
                       FOREIGN KEY (id_ingredient) REFERENCES ingredient
                     );

CREATE TABLE carte (
                       id_menu   INTEGER NOT NULL,
                       id_plat INTEGER NOT NULL,
                     --  PRIMARY KEY (id_menu, id_plat),
                       FOREIGN KEY (id_menu)   REFERENCES menu,
                       FOREIGN KEY (id_plat) REFERENCES plat
                     );

CREATE TABLE liste (
                       id_restaurant   INTEGER NOT NULL,
                       id_menu INTEGER NOT NULL,
                       --PRIMARY KEY (id_restaurant, id_menu),
                       FOREIGN KEY (id_restaurant)   REFERENCES restaurant,
                       FOREIGN KEY (id_menu) REFERENCES menu
                     );

